						</h2>
						<p>
							Ο Χαγιάο Μιγιαζάκι (ιαπωνικά: 宮崎 駿‎, Miyazaki Hayao), γεννημένος στις 5 Ιανουαρίου του 1941 στο Τόκιο της Ιαπωνίας, είναι ο διαπρεπής σκηνοθέτης πολλών γνωστών και δημοφιλών ταινιών κινουμένων σχεδίων (anime).

	<br/>Ο Μιγιαζάκι αν και ξεκίνησε να παρουσιάζει δείγματα της δουλειάς του ήδη από τη δεκαετία του 1970, παρέμενε σχετικά άγνωστος στη δύση μέχρι το 1999 που η Miramax διένειμε στις Η.Π.Α και τον υπόλοιπο κόσμο την ταινία του Πριγκίπισσα Μονονόκε. Εκείνη την εποχή οι ταινίες του Μιγιαζάκι ήταν ήδη εμπορικές και καλλιτεχνικές επιτυχίες στην πατρίδα του την Ιαπωνία αλλά και στην υπόλοιπη ανατολική Ασία. Το Spirited Away (2001) είναι η πιο επιτυχημένη εμπορικά ταινία όλων των εποχών στην Ιαπωνία, ενώ στην ίδια θέση είχε βρεθεί παλιότερα και η Πριγκίπισσα Μονονόκε.

	<br/>Οι ταινίες του Μιγιαζάκι συχνά περιστρέφονται γύρω από κοινά θέματα, όπως η σχέση των ανθρώπων με τη φύση και την τεχνολογία και η δυσκολία επικράτησης μιας ειρηνιστικής δεοντολογίας. Οι πρωταγωνίστριές του είναι συνήθως ανεξάρτητα και δυναμικά κορίτσια ή νεαρές γυναίκες, ενώ οι κακοί έχουν αμφισήμαντους χαρακτήρες και κάποια καλά στοιχεία.
