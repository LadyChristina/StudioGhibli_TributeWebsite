				<h1>
				 Studio Ghibli.
				</h1>
				<p>
				Το Studio Ghibli, Inc. (Ιαπωνικά: 株式会社 ス イ ジ ブ リ Hepburn: Kabushiki-gaisha Sutajio Jiburi) είναι ένα ιαπωνικό κινηματογραφικό στούντιο κινούμενων σχεδίων που εδρεύει στο Koganei του Τόκιο της Ιαπωνίας. Το στούντιο είναι γνωστό για τις μεγάλες ταινίες μεγάλου μήκους του και έχει επίσης δημιουργήσει αρκετές ταινίες μικρού μήκους, τηλεοπτικές διαφημίσεις και μία τηλεοπτική ταινία. Ιδρύθηκε στις 15 Ιουνίου 1985, μετά την επιτυχία της Nausicaa της κοιλάδας του ανέμου (1984), με χρηματοδότηση από τον Tokuma Shoten.
				</p>
				<p>
				Οκτώ από τις ταινίες του Studio Ghibli συγκαταλέγονται στις 15 καλύτερες ταινίες ανιχνεύσεων που έγιναν στην Ιαπωνία, με το Spirited Away (2001) να είναι το δεύτερο υψηλότερο με κέρδη άνω των 290 εκατομμυρίων δολαρίων παγκοσμίως. Πολλά από τα έργα τους έχουν κερδίσει το βραβείο Animation Anime Grand Prix και τέσσερα έχουν κερδίσει το βραβείο Ιαπωνίας για την ταινία Animation of the Year. Πέντε ταινίες του Studio Ghibli έλαβαν υποψηφιότητες για Όσκαρ στις Ηνωμένες Πολιτείες. Το Spirited Away κέρδισε μια Χρυσή Αρκούδα το 2002 και το Βραβείο Όσκαρ για την Καλύτερη Κινούμενη Κινηματογραφική Ταινία το 2003. Το Totoro, ένας χαρακτήρας από τον γείτονα Totoro, είναι η μασκότ του στούντιο.
				</p>
				<p>
				Στις 3 Αυγούστου 2014, το Studio Ghibli ανακοίνωσε ότι προσωρινά σταμάτησε την παραγωγή μετά τη συνταξιοδότηση του σκηνοθέτη Hayao Miyazaki, ο οποίος ίδρυσε το στούντιο με τον Isao Takahata. Τον Φεβρουάριο του 2017, ο Toshio Suzuki ανακοίνωσε ότι ο Hayao Miyazaki είχε βγει και πάλι από τη συνταξιοδότησή του για να κατευθύνει μια νέα ταινία μεγάλου μήκους με το Studio Ghibli.
