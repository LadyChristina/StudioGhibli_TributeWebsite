				<h1>Διακρίσεις του Studio Ghibli </h1>
				<article id="awards_article" >
					<p>
						Κατά τη διάρκεια των 32 χρόνων δημιουργίας ταινιών κινουμένων σχεδίων (από το 1985), το Studio Ghibli έχει λάβει πολλά βραβεία και υποψηφιότητες για τις ταινίες του.
					</p>
					<p>
						Οκτώ από τις ταινίες του Studio Ghibli συγκαταλέγονται στις 15 πιο εμπορικά επιτυχημένες ταινίες ιαπωικής προέλευσης, με το Spirited Away (2001) να είναι δεύτερη, με κέρδη άνω των 290 εκατομμυρίων δολαρίων παγκοσμίως. Πολλά από τα έργα τους έχουν κερδίσει το βραβείο Animage Anime Grand Prix και τέσσερα κέρδισαν το Βραβείο Ιαπωνικής Ακαδημίας για το Animation of the Year. Πέντε ταινίες του Studio Ghibli έλαβαν υποψηφιότητες για Όσκαρ στις Ηνωμένες Πολιτείες. Το Spirited Away κέρδισε το 2002 μια Χρυσή Αρκούδα και το 2003 βραβείο Όσκαρ για την καλύτερη κινηματογραφική ταινία κινουμένων σχεδίων.
					</p>
					<p>
						Παρακάτω παρατίθεται μια λίστα με όλες τις ταινίες του Studio Ghibli που έχουν κερδίσει ή ήταν υποψήφιες για ένα βραβείο.
					</p>
