          <h1>
          Η ιστορία πίσω από τον θρύλο
          </h1>
          <p>
		  Έχοντας ιδρυθεί στις 15 Ιουνίου 1985, το στούντιο διευθύνεται από τους σκηνοθέτες <a href="people.php#Miyazaki"> Hayao Miyazaki </a> και <a href="people.php#Takahata"> Isao Takahata </a > και τον παραγωγό <a href="people.php#Suzuki"> Toshio Suzuki </a>. Πριν από τη δημιουργία του στούντιο, οι Miyazaki και Takahata είχαν ήδη μακρά σταδιοδρομία σε ιαπωνικά κινηματογραφικά και τηλεοπτικά animations και είχαν συνεργαστεί στο Hols: Prince of the Sun και Panda! Go, Panda!, και ο Suzuki ήταν συντάκτης του περιοδικού Manage Magazine του Tokuma Shoten.
          </p>
          <p>
		  Το στούντιο ιδρύθηκε μετά την επιτυχία της ταινίας Nausicaä of the Valley of the Wind το 1984, γραμμένο και σε σκηνοθεσία Miyazaki για το Topcraft και διανεμημένο από την Toei Company. Οι ρίζες της ταινίας βρίσκονται στους δύο πρώτους τόμους ενός manga που γράφτηκε από τον Miyazaki. O Suzuki ήταν μέλος της ομάδας παραγωγής της ταινίας και ίδρυσε το Studio Ghibli με τον Miyazaki, ο οποίος προσκάλεσε και τον Takahata να συμμετάσχει στο νέο στούντιο.
          </p>
          <p>
            Το στούντιο έχει παράγει κυρίως ταινίες του Miyazaki, με τον δεύτερο πιο παραγωγικό σκηνοθέτη να είναι ο Takahata (κυρίως με το Grave of the Fireflies). Άλλοι σκηνοθέτες που συνεργάστηκαν με το Studio Ghibli είναι οι: <a href="people.php#Yoshifumi_Kondō">Yoshifumi Kondō</a>, Hiroyuki Morita, <a href="people.php#Miyazaki_son">Gorō Miyazaki</a>, και <a href="people.php#Yonebayashi">Hiromasa Yonebayashi</a>. 
			Ο συνθέτης <a href="people.php#Hisaishi">Joe Hisaishi</a> παρείχε τα soundtrack για τις περισσότερες από τις ταινίες του Miyazaki. Στο βιβλίο τους Anime Classics Zettai!, ο Brian Camp και η Julie Davis σημείωσαν τον Michiyo Yasuda ως "στυλοβάτη της εξαιρετικής σχεδιαστικής και παραγωγικής ομάδας του Studio Ghibli". Κάποτε το στούντιο είχε την έδρα του στο Kichijōji, Musashino, Τόκιο.
          </p>
          <p>
            Τον Αύγουστο του 1996, η Disney και η Tokuma Shoten Publishing συμφώνησαν ότι η Disney θα διανείμει τις ταινίες κινουμένων σχεδίων του Studio Ghibli σε διεθνές επίπεδο.
		  </p>
          <p>
            Πολλές από τις ταινίες του Ghibli στην Ιαπωνία είναι θεατρικά διανεμημένες από το Toho ενώ τις εγχώριες κυκλοφορίες βίντεο διαχειρίζονται τα Walt Disney Studios Home Entertainment Japan. Το Wild Bunch κατέχει τα διεθνή δικαιώματα πωλήσεων σε πολλές από τις ταινίες του Ghibli. Οι κύριοι διεθνείς συνεργάτες διανομής του Ghibli περιλαμβάνουν επίσης τη Disney (AstroBoyShowCaseTheater (ιδιωτική) στη Βόρεια Αμερική, Japan Home Video, Ταϊβάν, Γαλλία), GKIDS (Βόρεια Αμερική (Home Media από το 2017)), StudioCanal ) και Madman Entertainment (Αυστραλία). Το ίδιο το Wild Bunch που εδρεύει στη Γερμανία χειρίζεται τις διεθνείς πωλήσεις για τα έργα του Studio Ghibli.
   		  </p>
          <p>
			Με τα χρόνια, υπήρξε στενή σχέση μεταξύ του Studio Ghibli και του περιοδικού Animage, το οποίο εκτελεί τακτικά αποκλειστικά άρθρα για το στούντιο και τα μέλη του σε μια ενότητα με τίτλο "Σημειώσεις Ghibli". Η εικονογράφηση από ταινίες και άλλα έργα του Ghibli εμφανίζεται συχνά στο εξώφυλλο του περιοδικού. Το μυθιστόρημα του Saeko Himuro Umi ga Kikoeru εμφανίστηκε στο περιοδικό και στη συνέχεια προσαρμόστηκε σε Ocean Waves (κινηματογραφική ταινία), τη μοναδική ταινία κινουμένων σχεδίων του Studio Ghibli που δημιουργήθηκε για την τηλεόραση και διευθύνθηκε από τον Tomomi Mochizuki. Μεταξύ 1999 και 2005 το Studio Ghibli ήταν θυγατρική του Tokuma Shoten, του εκδότη του Animage.
          </p>
          <p>
			Τον Οκτώβριο του 2001 άνοιξε το Μουσείο Ghibli στη Μιτάκα του Τόκιο. Περιέχει εκθέματα που βασίζονται σε ταινίες του Studio Ghibli και παρουσιάζει κινούμενα σχέδια, όπως μερικές μικρές ταινίες Studio Ghibli που δεν διατίθενται αλλού.
          </p>
          <p>
			Το στούντιο είναι επίσης γνωστό για την αυστηρή πολιτική του για την αδειοδότηση των ταινιών του στο εξωτερικό, λόγω του ότι η ταινία Nausicaä of the Valley of Wind επεξεργάστηκε έντονα για την κυκλοφορία της ταινίας στις Ηνωμένες Πολιτείες ως πολεμιστές του ανέμου. Η "χωρίς περικοπές" πολιτική επισημάνθηκε όταν ο συν-πρόεδρος της Miramax, Harvey Weinstein, πρότεινε την επεξεργασία της πριγκίπισσας Mononoke για να γίνει πιο εμπορεύσιμη. Ένας παραγωγός Studio Ghibli φημολογείται ότι έστειλε ένα αυθεντικό ιαπωνικό σπαθί με ένα απλό μήνυμα: "Όχι περικοπές".
          </p>
          <p>
			Την 1η Φεβρουαρίου 2008, ο <a href="people.php#suzuki"> Toshio Suzuki </a> παραιτήθηκε από τη θέση του προέδρου του Studio Ghibli, την οποία κατείχε από το 2005, και <a href = "people.php#Hoshino "> Koji Hoshino </a> (πρώην πρόεδρος της Walt Disney Japan) ανέλαβε. Ο Suzuki δήλωσε ότι θέλει να βελτιώσει τις ταινίες με τα δικά του χέρια ως παραγωγός, αντί να το απαιτεί από τους υπαλλήλους του. Ο Suzuki αποφάσισε να παραδώσει την προεδρία στον Hoshino επειδή ο Hoshino βοηθούσε το Studio Ghibli να πουλήσει τα βίντεό του από το 1996, βοηθώντας επίσης στην κυκλοφορία της ταινίας Princess Mononoke στις Ηνωμένες Πολιτείες. Ο Suzuki εξακολουθεί να υπηρετεί στο διοικητικό συμβούλιο της εταιρείας.
          </p>
          <p>
			Δύο ταινίες μικρού μήκους του Studio Ghibli που δημιουργήθηκαν για το Μουσείο Ghibli παρουσιάστηκαν στο Φεστιβάλ Carnegie Hall Citywise Japan NYC: οι ταινίες "House Hunting" και "Mon Mon the Water Spider" προβλήθηκαν στις 26 Μαρτίου 2011.
          </p>
          <p>
			Ο Takahata ανέπτυξε ένα έργο για κυκλοφορία μετά από το The Tale of the Princess Kaguya του <a href="people.php#Miyazaki_son"> Gorō Miyazaki </a> (διευθυντής του Tales from Earthsea και γιος του Hayao) - μια προσαρμογή του The Tale of the Bamboo Cutter. Η τελευταία ταινία που σκηνοθέτησε ο <a href="people.php#Miyazaki"> Hayao Miyazaki </a> πριν από τη συνταξιοδότησή του από τις μεγάλες ταινίες ήταν το The Wind Rises  που έχει να κάνει με το Mitsubishi A6M Zero και τον ιδρυτή του.
          </p>
          <p>
			Την Κυριακή 1 Σεπτεμβρίου 2013, ο <a href="people.php#Miyazaki"> Hayao Miyazaki </a> πραγματοποίησε συνέντευξη Τύπου στη Βενετία για να επιβεβαιώσει την συνταξιοδότησή του, λέγοντας: "Ξέρω ότι έχω πει ότι θα συνταξιοδοτηθώ πολλές φορές στο παρελθόν. Πολλοί από εσάς πρέπει να σκεέφτεστε: "Άντε πάλι". Αλλά αυτή τη φορά το εννοώ."
          </p>
          <p>
			Στις 31 Ιανουαρίου 2014, ανακοινώθηκε ότι ο Gorό Miyazaki θα κατευθύνει την πρώτη του τηλεοπτική σειρά anime, την Sanzoku no Musume Rōnya, μια προσαρμογή του Ronia the Robber's Daughter της Astrid Lindgren για το NHK. Η σειρά έχει παραχθεί σε υπολογιστή, από την Polygon Pictures σε συνπαραγωγή με το Studio Ghibli.
          </p>
          <p>
			Τον Μάρτιο του 2014, ο <a href="people.php#Suzuki"> Toshio Suzuki </a> αποσύρθηκε ως παραγωγός και ανέλαβε μια νέα θέση γενικού διευθυντή. Ο Yoshiaki Nishimura αντικατέστησε το Suzuki στο ρόλο του παραγωγού.
          </p>
          <p>		  
			Στις 3 Αυγούστου 2014, ο <a href="people.php#Suzuki"> Toshio Suzuki </a> ανακοίνωσε ότι το Studio Ghibli θα κάνει μια "σύντομη παύση" για να επανεκτιμηθεί και να αναδιαρθρωθεί μετά την αποχώρηση του Miyazaki. Δήλωσε κάποιες ανησυχίες για το πού θα πήγαινε η εταιρεία στο μέλλον. Αυτό έχει οδηγήσει σε εικασίες ότι το Studio Ghibli δεν θα παράγει ποτέ ξανά άλλη μια ταινία μεγάλου μήκους. Στις 7 Νοεμβρίου 2014, ο Miyazaki δήλωσε: "Αυτό δεν ήταν η πρόθεσή μου, όμως το μόνο που έκανα ήταν να ανακοινώσω ότι θα συνταξιοδοτηθώ και δεν θα έκανα άλλες ταινίες μεγάλου μήκος". Ο διευθύνων παραγωγός Yoshiaki Nishimura μεταξύ πολλών άλλων στελεχών της Ghibli έφυγε για να ιδρύσει το Studio Ponoc τον Απρίλιο του 2015, δουλεύοντας για την ταινία Mary and the Witch's Flower.
          </p>
          <p>		  
			Η εικονογραφημένη ταινία φαντασίας του 2016, The Red Turtle, που σκηνοθέτησε και συνσεναριογράφησε ο ολλανδός-βρετανός animator Michaël Dudok de Wit στο ντεμπούτο του σε ταινίες μεγάλου μήκους, ήταν μια συμπαραγωγή μεταξύ του Studio Ghibli και του Wild Bunch.
          </p>
          <p>
			Το Φεβρουάριο του 2017, ο <a href="people.php#Suzuki"> Toshio Suzuki </a> ανακοίνωσε ότι ο <a href="people.php#Miyazaki"> Hayao Miyazaki </a> επέστρεψε από τη συνταξιοδότηση για να σκηνοθετήει μια νέα ταινία μεγάλου μήκους με το Studio Ghibli.
          </p>
