          <article class="flex-component">
            <h2>Laputa: the castle in the sky (1986)</h2>
            <h5>Director: Hayao Miyazaki<br />Writer: Hayao Miyazaki</h5>
            <p>A young boy stumbles into a mysterious girl who floats down from the sky. The girl, Sheeta, was chased by pirates, army and government secret agents. In saving her life, they begin a high flying adventure that goes through all sorts of flying machines, eventually searching for Sheeta's identity in a floating castle of a lost civilization.</p>
            <figure class="movie-poster">
              <img src="../images/films/laputa.jpg" alt="Laputa poster" height="470" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>Grave of the fireflies (1988)</h2>
            <h5>Director: Isao Takahata<br />Writer: Isao Takahata</h5>
            <p>The story of Seita and Satsuko, two young Japanese siblings, living in the declining days of World War II. When an American firebombing separates the two children from their parents, the two siblings must rely completely on one another while they struggle to fight for their survival.</p>
            <figure class="movie-poster">
              <img src="../images/films/graveOfTheFireflies.jpg" alt="Grave of the fireflies poster" height="480" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>My neighbor Totoro (1988)</h2>
            <h5>Director: Hayao Miyazaki<br />Writer: Hayao Miyazaki</h5>
            <p>Two young girls, Satsuki and her younger sister Mei, move into a house in the country with their father to be closer to their hospitalized mother. Satsuki and Mei discover that the nearby forest is inhabited by magical creatures called Totoros. They soon befriend these Totoros, and have several magical adventures.</p>
            <figure class="movie-poster">
              <img src="../images/films/totoro.jpg" alt="My neighbor Totoro poster" height="460" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>Kiki's delivery service (1989)</h2>
            <h5>Director: Hayao Miyazaki<br />Writer: Hayao Miyazaki</h5>
            <p>This is the story of a young witch, named Kiki who is now thirteen years old. But she is still a little green and plenty headstrong, but also resourceful, imaginative, and determined. With her trusty wisp of a talking cat named Jiji by her side she's ready to take on the world, or at least the quaintly European seaside village she's chosen as her new home.</p>
            <figure class="movie-poster">
              <img src="../images/films/kiki.png" alt="Kiki's delivery service poster" height="460" width="340">
            </figure>
          </article>
          <article class="flex-component">
            <h2>Only yesterday (1991)</h2>
            <h5>Director: Isao Takahata<br />Writer: Isao Takahata</h5>
            <p>A twenty-seven-year-old office worker travels to the countryside while reminiscing about her childhood in Tokyo.</p>
            <figure class="movie-poster">
              <img src="../images/films/onlyYesterday.jpg" alt="Only yesterday poster" height="480" width="340">
            </figure>
          </article>
          <article class="flex-component">
            <h2>Porco Rosso (1992)</h2>
            <h5>Director: Hayao Miyazaki<br />Writer: Hayao Miyazaki</h5>
            <p>In Early 1930's era Italy air pirates, bounty hunters and high fliers of all sorts rule the skies. The most cunning and skilled of these pilots is Porco Rosso. A former Ace, he now makes a living flying contract jobs, such as rescuing those kidnapped by air pirates. Donald Curtis, Porco's rival in the air and in catching the affections of women, provides a constant challenge to the hero, culminating in a hilarious, action packed finale.</p>
            <figure class="movie-poster">
              <img src="../images/films/porcoRosso.jpg" alt="Porco Rosso poster" height="490" width="340">
            </figure>
          </article>
          <article class="flex-component">
            <h2>Pom Poko (1994)</h2>
            <h5>Director: Isao Takahata<br />Writer: Isao Takahata</h5>
            <p>As the human city development encroaches on the raccoon dog population's forest and meadow habitat, the raccoon dogs find themselves faced with the very real possibility of extinction. In response, the raccoon dogs engage in a desperate struggle to stop the construction and preserve their home.</p>
            <figure class="movie-poster">
              <img src="../images/films/pomPoko.png" alt="Pom Poko poster" height="480" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>Whisper of the heart (1995)</h2>
            <h5>Director: Yoshifumi Kondō<br />Writer: Hayao Miyazaki</h5>
            <p>A young girl finds that all the books she chooses in the library have been previously checked out by the same boy. Later she meets a very infuriating fellow... could it be her "friend" from the library? The boy's grandfather has a violin sales and service shop. The boy wants to be a violin maker like his grandfather.</p>
            <figure class="movie-poster">
              <img src="../images/films/whisperOfTheHeart.jpg" alt="Whisper of the heart poster" height="470" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>Princess Mononoke (1997)</h2>
            <h5>Director: Hayao Miyazaki<br />Writer: Hayao Miyazaki</h5>
            <p>While protecting his village from rampaging boar-god/demon, a confident young warrior, Ashitaka, is stricken by a deadly curse. To save his life, he must journey to the forests of the west. Once there, he's embroiled in a fierce campaign that humans were waging on the forest. The ambitious Lady Eboshi and her loyal clan use their guns against the gods of the forest and a brave young woman, Princess Mononoke, who was raised by a wolf-god. Ashitaka sees the good in both sides and tries to stem the flood of blood. This is met be animosity by both sides as they each see him as supporting the enemy</p>
            <figure class="movie-poster">
              <img src="../images/films/mononoke.jpg" alt="Princess Mononoke poster" height="470" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>My neighbors the Yamadas (1999)</h2>
            <h5>Director: Isao Takahata<br />Writer: Isao Takahata</h5>
            <p>The Yamadas are a typical middle class Japanese family in urban Tokyo and this film shows us a variety of episodes of their lives. With tales that range from the humourous to the heartbreaking, we see this family cope with life's little conflicts, problems and joys in their own way. </p>
            <figure class="movie-poster">
              <img src="../images/films/yamadas.jpg" alt="My neighbors the Yamadas poster" height="480" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>Spirited away (2001)</h2>
            <h5>Director: Hayao Miyazaki<br />Writer: Hayao Miyazaki</h5>
            <p>10 year-old Chihiro and her parents are riding along during a family outing through the countryside when they stumble across an amusement park that has seemingly been abandoned for years. Despite Chihiro's premonitions about the creepy setting, her parents explore the area and eventually discover and indulge in an empty eatery filled with fresh food. As a result of their unfaithfulness, they are magically turned into pigs, which in turn scares away Chihiro. She meets the enigmatic Haku, who explains to her that this land is actually a magical bath house, a kind of holiday resort, where supernatural beings seek comfort away from the earthly realm and she must work here, as laziness is not permitted, to free both herself and her parents from the mystical land.</p>
            <figure class="movie-poster">
              <img src="../images/films/spiritedAway.png" alt="Spirited Away poster" height="480" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>The cat returns (2002)</h2>
            <h5>Director: Hiroyuki Morita<br />Writer: Reiko Yoshida</h5>
            <p>A young girl rescues a mysterious cat from traffic and soon finds herself the unwelcome recipient of gifts and favors from the King of the Cats, who also wants her to marry his son, Prince Lune. With the assistance of a fat, grouchy real cat and a an elegant cat statuette come to life (both characters featured in Studio Ghibli's earlier anime "Whisper of the Heart"), the girl visits the Cat Kingdom and narrowly escapes again. </p>
            <figure class="movie-poster">
              <img src="../images/films/theCatReturns.jpg" alt="The cat returns poster" height="460" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>Howl's moving castle (2004)</h2>
            <h5>Director: Hayao Miyazaki<br />Writer: Hayao Miyazaki</h5>
            <p>A love story between an 18-year-old girl named Sophie, cursed by a witch into an old woman's body, and a magician named Howl. Under the curse, Sophie sets out to seek her fortune, which takes her to Howl's strange moving castle. In the castle, Sophie meets Howl's fire demon, named Karushifâ. Seeing that she is under a curse, the demon makes a deal with Sophie--if she breaks the contract he is under with Howl, then Karushifâ will lift the curse that Sophie is under, and she will return to her 18-year-old shape.</p>
            <figure class="movie-poster">
              <img src="../images/films/howlsMovingCastle.jpeg" alt="Howl's moving castle poster" height="460" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>Tales from Earthsea (2006)</h2>
            <h5>Director: Gorō Miyazaki<br />Writer: Ursula K Le Guin, Gorō Miyazaki, Keiko Niwa</h5>
            <p>Something bizarre has come over the land. The kingdom is deteriorating. People are beginning to act strange... What's even more strange is that people are beginning to see dragons, which shouldn't enter the world of humans. Due to all these bizarre events, Ged, a wandering wizard, is investigating the cause. During his journey, he meets Prince Arren, a young distraught teenage boy. While Arren may look like a shy young teen, he has a severe dark side, which grants him strength, hatred, ruthlessness and has no mercy, especially when it comes to protecting Teru. For the witch Kumo this is a perfect opportunity. She can use the boy's "fears" against the very one who would help him, Ged.</p>
            <figure class="movie-poster">
              <img src="../images/films/talesFromEarthsea.jpg" alt="Tales from Earthsea poster" height="510" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>Ponyo (2008)</h2>
            <h5>Director: Hayao Miyazaki<br />Writer: Hayao Miyazaki</h5>
            <p>The son of a sailor, 5-year old Sosuke lives a quiet life on an oceanside cliff with his mother Lisa. One fateful day, he finds a beautiful goldfish trapped in a bottle on the beach and upon rescuing her, names her Ponyo. But she is no ordinary goldfish. The daughter of a masterful wizard and a sea goddess, Ponyo uses her father's magic to transform herself into a young girl and quickly falls in love with Sosuke, but the use of such powerful sorcery causes a dangerous imbalance in the world. As the moon steadily draws nearer to the earth and Ponyo's father sends the ocean's mighty waves to find his daughter, the two children embark on an adventure of a lifetime to save the world and fulfill Ponyo's dreams of becoming human.</p>
            <figure class="movie-poster">
              <img src="../images/films/ponyo.jpg" alt="Ponyo poster" height="460" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>Arrietty (2010)</h2>
            <h5>Director: Hiromasa Yonebayashi<br />Writer: Hayao Miyazaki, Keiko Niwa</h5>
            <p>14-year-old Arrietty and the rest of the Clock family live in peaceful anonymity as they make their own home from items that they borrow from the house's human inhabitants. However, life changes for the Clocks when a human boy discovers Arrietty.</p>
            <figure class="movie-poster">
              <img src="../images/films/arrietty.png" alt="Arrietty poster" height="460" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>From up on poppy hill (2011)</h2>
            <h5>Director: Gorō Miyazaki<br />Writer: Hayao Miyazaki, Keiko Niwa</h5>
            <p>A group of Yokohama teens look to save their school's clubhouse from the wrecking ball in preparations for the 1964 Tokyo Olympics.</p>
            <figure class="movie-poster">
              <img src="../images/films/fromUpOnPoppyHill.jpg" alt="From up on poppy hill poster" height="460" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>The wind rises (2013)</h2>
            <h5>Director: Hayao Miyazaki<br />Writer: Hayao Miyazaki</h5>
            <p>Jiro dreams of flying and designing beautiful airplanes, inspired by the famous Italian aeronautical designer Caproni. Nearsighted from a young age and unable to be a pilot, Jiro joins a major Japanese engineering company in 1927 and becomes one of the world's most innovative and accomplished airplane designers. The film chronicles much of his life, depicting key historical events, including the Great Kanto Earthquake of 1923, the Great Depression, the tuberculosis epidemic and Japan's plunge into war. Jiro meets and falls in love with Nahoko, and grows and cherishes his friendship with his colleague Honjo</p>
            <figure class="movie-poster">
              <img src="../images/films/theWindRises.jpg" alt="The wind rises poster" height="480" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>The tale of the princess Kaguya (2013)</h2>
            <h5>Director: Isao Takahata<br />Writer: Isao Takahata, Riko Sakaguchi</h5>
            <p>An old man makes a living by selling bamboo. One day, he finds a princess in a bamboo. The princess is only the size of a finger. Her name is Kaguya. When Kaguya grows up, 5 men from prestigious families propose to her. Kaguya asks the men to find memorable marriage gifts for her, but the 5 men are unable to find what Kaguya wants. Then, the Emperor of Japan proposes to her.</p>
            <figure class="movie-poster">
              <img src="../images/films/Kaguya.jpg" alt="The tale of princess Kaguya poster" height="480" width="340">
            </figure>
          </article>  
          <article class="flex-component">
            <h2>When Marnie was there (2014)</h2>
            <h5>Director: Hiromasa Yonebayashi<br />Writer: Hiromasa Yonebayashi, Keiko Niwa, Masashi Ando</h5>
            <p>A young girl is sent to the country for health reasons, where she meets an unlikely friend in the form of Marnie, a young girl with flowing blonde hair. As the friendship unravels it is possible that Marnie has closer ties to the protagonist than we might expect.</p>
            <figure class="movie-poster">
              <img src="../images/films/Marnie.jpg" alt="When Marnie was there poster" height="480" width="340">
            </figure>
