          <h1>
          The story behind the legend
          </h1>
          <p>
            Founded on June 15, 1985, the studio is headed by the directors <a href="people.php#Miyazaki">Hayao Miyazaki</a> and <a href="people.php#Takahata">Isao Takahata</a> and the producer <a href="people.php#Suzuki">Toshio Suzuki</a>. Prior to the formation of the studio, Miyazaki and Takahata had already had long careers in Japanese film and television animation and had worked together on Hols: Prince of the Sun and Panda! Go, Panda!; and Suzuki was an editor at Tokuma Shoten's Animage manga magazine.
          </p>
          <p>
            The studio was founded after the success of the 1984 film Nausicaä of the Valley of the Wind, written and directed by Miyazaki for Topcraft and distributed by Toei Company. The origins of the film lie in the first two volumes of a serialized manga written by Miyazaki. Suzuki was part of the production team on the film and founded Studio Ghibli with Miyazaki, who also invited Takahata to join the new studio.
          </p>
          <p>
            The studio has mainly produced films by Miyazaki, with the second most prolific director being Takahata (most notably with Grave of the Fireflies). Other directors who have worked with Studio Ghibli include <a href="people.php#Yoshifumi_Kondō">Yoshifumi Kondō</a>, Hiroyuki Morita, <a href="people.php#Miyazaki_son">Gorō Miyazaki</a>, and <a href="people.php#Yonebayashi">Hiromasa Yonebayashi</a>. Composer <a href="people.php#Hisaishi">Joe Hisaishi</a> has provided the soundtracks for most of Miyazaki's Studio Ghibli films. In their book Anime Classics Zettai!, Brian Camp and Julie Davis made note of Michiyo Yasuda as "a mainstay of Studio Ghibli’s extraordinary design and production team". At one time the studio was based in Kichijōji, Musashino, Tokyo.
          </p>
          <p>
            In August 1996, Disney and Tokuma Shoten Publishing agreed that Disney would distribute internationally Tokuma's Studio Ghibli animated films.
          </p>
          <p>
            Many of Ghibli's films in Japan are theatrically distributed by Toho while home video releases are handled by Walt Disney Studios Home Entertainment Japan. Wild Bunch holds the international sales rights to many of Ghibli's films. Ghibli's main international distribution partners also include Disney (AstroBoyShowCaseTheater (private) in North America, Japan Home Video, Taiwan, France), GKIDS (North America (Home Media since 2017)), StudioCanal (United Kingdom and Ireland), and Madman Entertainment (Australia). Germany-based Wild Bunch itself handles international sales for Studio Ghibli's works.
          </p>
          <p>
            Over the years, there has been a close relationship between Studio Ghibli and the magazine Animage, which regularly runs exclusive articles on the studio and its members in a section titled "Ghibli Notes." Artwork from Ghibli's films and other works are frequently featured on the cover of the magazine. Saeko Himuro's novel Umi ga Kikoeru was serialised in the magazine and subsequently adapted into Ocean Waves (film), Studio Ghibli's only animated feature length film created for television and it was directed by Tomomi Mochizuki. Between 1999 and 2005 Studio Ghibli was a subsidiary of Tokuma Shoten, the publisher of Animage.
          </p>
          <p>
            In October 2001, the Ghibli Museum opened in Mitaka, Tokyo. It contains exhibits based on Studio Ghibli films and shows animations, including a number of short Studio Ghibli films not available elsewhere.
          </p>
          <p>
            The studio is also known for its strict "no-edits" policy in licensing their films abroad due to Nausicaä of the Valley of Wind being heavily edited for the film's release in the United States as Warriors of the Wind. The "no cuts" policy was highlighted when Miramax co-chairman Harvey Weinstein suggested editing Princess Mononoke to make it more marketable. A Studio Ghibli producer is rumoured to have sent an authentic Japanese sword with a simple message: "No cuts".
          </p>
          <p>
            On February 1, 2008, <a href="people.php#Suzuki">Toshio Suzuki</a> stepped down from the position of Studio Ghibli president, which he had held since 2005, and <a href="people.php#Hoshino">Koji Hoshino</a> (former president of Walt Disney Japan) took over. Suzuki said he wanted to improve films with his own hands as a producer, rather than demanding this from his employees. Suzuki decided to hand over the presidency to Hoshino because Hoshino has helped Studio Ghibli to sell its videos since 1996, also helping to release the Princess Mononoke film in the United States. Suzuki still serves on the company's board of directors.
          </p>
          <p>
            Two Studio Ghibli short films created for the Ghibli Museum were shown at the Carnegie Hall Citywise Japan NYC Festival: "House Hunting" and "Mon Mon the Water Spider" were screened on March 26, 2011.
          </p>
          <p>
            Takahata developed a project for release after <a href="people.php#Miyazaki_son">Gorō Miyazaki</a>'s (director of Tales from Earthsea and Hayao's son) The Tale of the Princess Kaguya – an adaptation of The Tale of the Bamboo Cutter. The last film <a href="people.php#Miyazaki">Hayao Miyazaki</a> directed before retiring from feature films was The Wind Rises which is about the Mitsubishi A6M Zero and its founder.
          </p>
          <p>
            On Sunday, September 1, 2013, <a href="people.php#Miyazaki">Hayao Miyazaki</a> held a press conference in Venice to confirm his retirement, saying: "I know I've said I would retire many times in the past. Many of you must think, 'Once again.' But this time I am quite serious."
          </p>
          <p>
            On January 31, 2014, it was announced that <a href="people.php#Miyazaki_son">Gorō Miyazaki</a> will direct his first anime television series, Sanzoku no Musume Rōnya, an adaptation of Astrid Lindgren's Ronia the Robber's Daughter for NHK. The series is computer-animated, produced by Polygon Pictures, and co-produced by Studio Ghibli.
          </p>
          <p>
            In March 2014, <a href="people.php#Suzuki">Toshio Suzuki</a> retired as a producer and assumed a new position of general manager. Yoshiaki Nishimura replaced Suzuki in the producer role.
          </p>
          <p>
            On August 3, 2014, <a href="people.php#Suzuki">Toshio Suzuki</a> announced that Studio Ghibli would take a "brief pause" to re-evaluate and restructure in the wake of Miyazaki's retirement. He stated some concerns about where the company would go in the future. This has led to speculation that Studio Ghibli will never produce another feature film again. On November 7, 2014, Miyazaki stated, "That was not my intention, though. All I did was announce that I would be retiring and not making any more features." Lead producer Yoshiaki Nishimura among several other staffers from Ghibli left to found Studio Ponoc in April 2015, working on the film Mary and the Witch's Flower.
          </p>
          <p>
            The 2016 animated fantasy film The Red Turtle, directed and co-written by Dutch-British animator Michaël Dudok de Wit in his feature film debut, was a co-production between Studio Ghibli and Wild Bunch.
          </p>
          <p>
            In February 2017, <a href="people.php#Suzuki">Toshio Suzuki</a> announced that <a href="people.php#Miyazaki">Hayao Miyazaki</a> has come out of retirement to direct a new feature film with Studio Ghibli.
          </p>
