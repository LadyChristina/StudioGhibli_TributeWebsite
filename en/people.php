								Co-Founder  <span class="role_name">Toshio Suzuki</span>
							</h2>
							<p>
								Toshio Suzuki (鈴木 敏夫Suzuki Toshio, born August 19 1948) is chief producer and co-founder of Studio Ghibli, he was president of Ghibli from 2005 to 2008. Suzuki is renowned as one of Japan's most successful producers after the enormous box office success (in Japan) of many Ghibli films. According to Hayao Miyazaki, "If it were not for Mr. Suzuki, there wouldn't have been Studio Ghibli."
								<br/>Suzuki started out at Tokuma Shoten joining the company shortly after graduating. He worked on Asahi Geino magazen before becoming editor of Animage in 1978, where he met Hayao Miyazaki who had created the Nausicaä of the Valley of the Wind manga. He convinced Miyazaki into turning Nausicaä into a film, co-founding Studio Ghbli after the film. He served on production committees on My Neighbor Totoro and Grave of the Fireflies before officially joining the studio in 1991 as a producer.
								<br/>He graduated from Keio University with a degree in literature.
							</p>					
