								Isao Takahata  <span class="role_name">Co-Founder</span>
							</h2>
							<p>
								Isao Takahata (高畑 勲 Takahata Isao, born October 29 1935) is a Japanese film director, animator, screenwriter and producer who has earned critical international acclaim for his work as a director of anime films. Takahata is the co-founder of Studio Ghibli along with long-time collaborative partner Hayao Miyazaki.
								<br/>Graduating from the University of Tokyo with a degree in French literature course in 1959, Takahata joined the newly-created Tōei Dōga animation company (Toei Animation) where a short time later he met Miyazaki, and also directed his first feature film Hols: Prince of the Sun. Ostracized within the company after the financial failure of the film (despite its artistic success), he and Miyazaki left in order to work together, and collaborated on many other films. Unlike most anime directors, Takahata doesn't draw and never worked as an animator before becoming a full fledged director.
								<br/> According to Hayao Miyazaki, "Music and study are his hobbies".
							</p>					
