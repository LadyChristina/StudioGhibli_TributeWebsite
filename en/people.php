							<p>
								Hayao Miyazaki (Japanese: 宮崎 駿 Hepburn: Miyazaki Hayao, born January 5 1941) is a Japanese film director, producer, screenwriter, animator, author, and manga artist. A co-founder of Studio Ghibli, a film and animation studio, he has attained international acclaim as a masterful storyteller and as a maker of anime feature films, and is widely regarded as one of the greatest animation directors.
								<br/>Miyazaki's films usually have common themes among them, including the typical struggle between good and evil, environmentalism, and politics. The protagonists are usually strong, independent girls or young women and the villains are typically uncertain in nature with redeeming qualities.
								<br/>He was announced to retire right after his last feature film, The Wind Rises, but will continue to work on short films.
							</p>
