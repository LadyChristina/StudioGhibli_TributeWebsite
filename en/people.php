							<h2>
								Animator  <span class="role_name"> Yoshifumi Kondō</span>
							</h2>
							<p>
							Yoshifumi Kondō (近藤 喜文 Kondō Yoshifumi, March 31 1950 – January 21 1998) was a Japanese animator who worked for Studio Ghibli in his last years. Kondō directed the animated film Whisper of the Heart, and was expected to become one of the top directors at Studio Ghibli, alongside Hayao Miyazaki and Isao Takahata, and to become their eventual successor.<br/>
							Kondō died of aortic dissection in 1998. His death is said to have been caused by excess work, and seems to be the main reason for Hayao Miyazaki's first announcement of retirement in 1998. Although Miyazaki later returned to Studio Ghibli, Kondō's death seems to have influenced Miyazaki to work at a more relaxed pace.
							</p>					
