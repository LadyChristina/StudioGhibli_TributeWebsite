								Co-Founder  <span class="role_name">Yasuyoshi Tokuma</span>
							</h2>
							<p>
								Tokuma Yasuyoshi (born October 25 1921, died September 20 2000) was a producer and the president of Tokuma Shoten and the Tokuma Group, as well as at a time president of the Daiei Motion Picture Company, Studio Ghibli and a supervisor at Tokyo Metropolitan Museum of Photography.<br/>
								Tokuma became the president of Tokuma Shoten, the parent company of Studio Ghibli, in 1954. Before becoming president, he had worked as a reporter for the Yomiuri Shinbun and as the president of another printing house. Tokuma produced many films while president, including all of the Studio Ghibli films except Grave of the Fireflies.
								<br/>Tokuma passed away at a Tokyo hospital on Wednesday, September 20, 2000. He was 78 years old.
							</p>					
