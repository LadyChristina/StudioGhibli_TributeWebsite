							</h2>
							<p>
							Yasuo Ōtsuka(大塚康生 Ootsuka Yasuo, born July 11 1931) has been a colleague and mentor to both Hayao Miyazaki and Isao Takahata since the 1960s. In his animation career he has drawn and sketched storyboards, handled continuity, layouts and eyecatches, written screenplays, and worked as a key animator, character designer, supervisor, and director. <br/>
							Since retiring in 1981, Ōtsuka has been fostering young animators as an instructor and advisor at Telecom Animation Films. Since 2001 he has been headmaster of the studio's online animation school, Anime Juku.
